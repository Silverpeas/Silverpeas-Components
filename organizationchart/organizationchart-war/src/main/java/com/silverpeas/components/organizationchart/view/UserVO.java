package com.silverpeas.components.organizationchart.view;

import java.util.Map;

public class UserVO {
  private String fullName = null;
  private String login = null;
  private String role = null;
  private Map<String, String> details = null;

  /**
   * @return the details
   */
  public Map<String, String> getDetails() {
    return details;
  }

  /**
   * @param details the details to set
   */
  public void setDetails(Map<String, String> details) {
    this.details = details;
  }

  public UserVO(String fullName, String login, String role) {
    this(fullName, login, role, null);
  }

  public UserVO(String fullName, String login, String role, Map<String, String> details) {
    super();
    this.fullName = fullName;
    this.login = login;
    this.role = role;
    this.details = details;
  }

  /**
   * @return the role
   */
  public String getRole() {
    return role;
  }

  /**
   * @param role the role to set
   */
  public void setRole(String role) {
    this.role = role;
  }

  /**
   * @return the fullName
   */
  public String getFullName() {
    return fullName;
  }

  /**
   * @param fullName the fullName to set
   */
  public void setFullName(String fullName) {
    this.fullName = fullName;
  }

  /**
   * @return the login
   */
  public String getLogin() {
    return login;
  }

  /**
   * @param login the login to set
   */
  public void setLogin(String login) {
    this.login = login;
  }

}
